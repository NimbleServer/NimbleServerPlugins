package de.nimble.server.player;

public class NimbleExperience {

  private int level;
  private double amount;

  public void setLevel(int level) {
    this.level = level;
  }

  public int getLevel() {
    return this.level;
  }

  public void setAmount(double amount) {
    this.amount = amount;
  }

  public double getAmount() {
    return this.amount;
  }
}
